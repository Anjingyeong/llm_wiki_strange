import { readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));

const searchJson = JSON.parse(
  await readFile(join(root, 'src/generated/searchIndex.json'), 'utf8'),
);
const ragManifest = JSON.parse(
  await readFile(join(root, 'data/rag/index-manifest.json'), 'utf8'),
);

const benchmark = {
  operationalRetrieval: 'Hybrid RRF (BM25 + local hash vector), k=60, lex=1, vec=1',
  diversify: 'max 2 chunks per document in production answer path',
  embedding: '256-d local hash embedding (no paid API)',
  chunking: 'structure-aware-v1 (heading-aware sections)',
  abstention: 'insufficient_context when evidence weak; source dedupe by slug',
  stage3Eval: {
    note: 'Offline golden set (55 queries); Stage-3 did not promote over vector-only under policy',
    hybridRrf: { hitAt5: 0.8, recallAt5: 0.55, mrr: 0.6967, duplicateResult: 50, wrongTop1: 9 },
    hybridDiversifyMax2: { hitAt5: 0.86, recallAt5: 0.6233, mrr: 0.7167, duplicateResult: 91 },
    diversifyMax1: { hitAt5: 0.86, recallAt5: 0.6767, duplicateChunkDup: 29 },
  },
  clientSearch: {
    threshold: 18,
    goldenQueries: 21,
    features: ['slug dedupe', 'Korean aliases', 'develop baseline query'],
  },
};

const payload = {
  generatedAt: new Date().toISOString(),
  search: searchJson.meta,
  searchDocCount: searchJson.documents?.length ?? searchJson.meta?.documentCount,
  rag: {
    schemaVersion: ragManifest.schemaVersion,
    chunkCount: ragManifest.chunkCount,
    documentCount: ragManifest.documentCount,
    corpusHash: ragManifest.corpusHash,
    meanChunkChars: ragManifest.meanChunkChars,
    operationalPath: ragManifest.operationalPath,
    indexGeneratedAt: ragManifest.generatedAt,
  },
  benchmark,
};

const outTs = join(root, 'src/generated/wikiUxMeta.ts');
const outPublic = join(root, 'public/wiki-ux-meta.json');

await writeFile(
  outTs,
  `/** Auto-generated by scripts/generate-wiki-ux-meta.mjs */\nexport const wikiUxMeta = ${JSON.stringify(payload, null, 2)} as const;\n`,
  'utf8',
);
await writeFile(outPublic, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
console.log('Generated wikiUxMeta.ts and public/wiki-ux-meta.json');